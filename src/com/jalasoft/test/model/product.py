class Product:
    def getProductName(self):
        return self.__productName

    def setProductName(self, productName):
        self.__productName = productName

    def getPrice(self):
        return self.__price

    def setPrice(self, price):
        self.__price = price

    def getId(self):
        return self.__Id

    def setId(self, Id):
        self.__Id = Id


